-- https://github.com/nvimdev/dashboard-nvim/blob/master/lua/dashboard/theme/header.lua#L92
local function set_week_header(opts)
    local daysoftheweek = { "sunday", "monday", "tuesday", "wednesday", "thursday", "friday", "saturday" }
    local day = daysoftheweek[os.date("*t").wday]
    local tbl = week_ascii_text[day]
    table.insert(tbl, os.date("%Y-%m-%d %H:%M:%S "))
    table.insert(tbl, "")
    opts.config.header = tbl
end

return {
    "nvimdev/dashboard-nvim",
    dependencies = { { "nvim-tree/nvim-web-devicons" } },
    event = "VimEnter",
    opts = {
        config = {
            shortcut = {
                -- { desc = "󰊳 Lazy", group = "@property", action = "Lazy", key = "l" },
            },
        },
    },
    config = function(_, opts)
        set_week_header(opts)
        require("dashboard").setup(opts)
    end,
}
